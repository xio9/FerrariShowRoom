        <h2>5 интересных фактов о Ферари</h2>
        <ul class="Facts">
            <li>Энцо Феррари родился 18 февраля 1898 года неподалеку от Модены. Его дед торговал овощами, а отец Альфредо работал рихтовщиком в местной железнодорожной мастерской.</li>
            <li>Свою первую гонку Энцо увидел в 10 лет, а в 13 он уже умел водить. Во время Первой Мировой Войны он был уволен в запас по инвалидности. Его отец и брат погибли на войне.</li>
            <li>В 1920 году он был принят гонщиком в команду Alfa Romeo и в тот же год пришел вторым на Targa Florio. В 1929 году он перестал выступать, став управляющим гоночной команды Alfa.</li>
            <li>Одним из самых странных проектов Феррари для Альфы был Bimotore – одноместный болид с двумя двигателями спереди и сзади. Она была очень быстрой, но практически неуправляемой.</li>
            <li>В те редкие минуты его жизни, которые не были посвящены автоспорту, Энцо любил кататься на своем любимом британском мотоцикле Rudge.</li>
        </ul>
